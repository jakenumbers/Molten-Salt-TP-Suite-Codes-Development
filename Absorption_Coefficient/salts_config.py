from dataclasses import dataclass
from typing import Dict, List, Tuple, Optional, Any

# =============================================================================
# 1. DATA CLASSES
# =============================================================================

@dataclass
class IonPairParams:
    ion_pair: Tuple[str, str]
    ion_pair_str: str
    epsilon_s: float
    epsilon_inf: float
    gamma0: float
    gamma_slope: float
    mole_fraction: float
    omega0: float = 0.0
    k_N_per_m: float = 0.0
    delta_eps: float = 0.0
    alpha_anh: float = 3.0
    C0_multi: float = 1e4

@dataclass
class SaltConfig:
    name: str
    ion_pairs: List[IonPairParams]
    eps_inf_mixture: float
    W0_eV: float
    kappa0: float
    rho_coeffs: Tuple[float, float]
    T_fus: float
    pdf_file: str
    T_pdf: float
    experimental_data: Optional[List[Dict[str, Any]]] = None
    experimental_n: Optional[Dict[str, Any]] = None
    experimental_fit_config: Optional[Dict[str, Any]] = None
    piecewise_fit: Optional[Dict[str, Any]] = None
    ir_peaks_um: Optional[List[Tuple[float, str]]] = None

SALT_REGISTRY: Dict[str, SaltConfig] = {}

def register_salt(config: SaltConfig):
    SALT_REGISTRY[config.name] = config

