"""
MSR Fuel Channel Radiative Heat Transfer Case Study
====================================================
Solves the 1D steady-state radial energy equation in a cylindrical
molten salt fuel channel with volumetric fission heat generation.

Physics:
    The governing equation in cylindrical coordinates is:

        (1/r) d/dr [ r k_eff(T) dT/dr ] + q''' = 0

    where:
        k_eff(T) = k_phonon(T) + k_rad(T)
        k_phonon(T) = k_ref * (T_fus / T)^0.4    (Gheribi model)
        k_rad(T) = 16 n^2 sigma T^3 / (3 kappa_P) (Rosseland diffusion)

    The Rosseland approximation is valid here because at the reactor
    channel scale (R ~ 5 cm), the optical thickness tau = kappa_P * R
    is typically >> 1 for molten salts (tau ~ 50-5000).

    Boundary conditions:
        r = 0:  dT/dr = 0   (centerline symmetry)
        r = R:  T = T_wall   (prescribed wall temperature)

    The comparison between conduction-only and conduction+radiation
    profiles shows:
        1. How much radiation reduces the peak centerline temperature
        2. The radial distribution of radiative heat transfer fraction
        3. The practical significance for MSR safety margins

Case Studies:
    Case 1 — Temperature profile comparison (conduction vs conduction+radiation)
    Case 2 — Radiative fraction across the channel radius

References:
    Gheribi et al. (2014) Sol. Energy Mater. Sol. Cells 126, 11-25.
    Chaleff et al. (2016) Nucl. Technol. 196(3), 1-9.
    Robertson et al. (2022) Appl. Phys. Lett. 131(22), 225102.
    Siegel & Howell (2002) Thermal Radiation Heat Transfer, 4th ed.

Dependencies:
    Requires 'Salt_RHT_Properties.csv' generated by the absorption model.
"""

