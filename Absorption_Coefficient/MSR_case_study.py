# 6. SUMMARY TABLE
# =============================================================================

def print_summary(salt_name, cfg, kappa_interp, n_interp):
    """Print key metrics for the channel solution."""
    # Solve both cases
    _, T_cond, _ = solve_channel(cfg, kappa_interp, n_interp, False)
    _, T_rad, k_eff_func = solve_channel(cfg, kappa_interp, n_interp, True)

    k_ph_center = k_phonon(T_rad[0], cfg)
    k_eff_center = k_eff_func(T_rad[0])
    k_rad_center = k_eff_center - k_ph_center
    frac = k_rad_center / k_eff_center * 100

    kappa_center = float(kappa_interp(T_rad[0]))
    tau = kappa_center * cfg['R_channel']

    print(f"\n  {'='*50}")
    print(f"  {salt_name} MSR Channel Summary")
    print(f"  {'='*50}")
    print(f"  Channel radius:          {cfg['R_channel']*100:.0f} cm")
    print(f"  Heat generation:         {cfg['q_vol']/1e6:.0f} MW/m^3")
    print(f"  Wall temperature:        {cfg['T_wall']:.0f} K")
    print(f"  {'─'*50}")
    print(f"  Peak T (conduction):     {T_cond[0]:.1f} K")
    print(f"  Peak T (cond+rad):       {T_rad[0]:.1f} K")
    print(f"  Temperature reduction:   {T_cond[0]-T_rad[0]:.1f} K")
    print(f"  {'─'*50}")
    print(f"  k_phonon (center):       {k_ph_center:.3f} W/m/K")
    print(f"  k_rad (center):          {k_rad_center:.3f} W/m/K")
    print(f"  k_eff (center):          {k_eff_center:.3f} W/m/K")
    print(f"  Radiative fraction:      {frac:.1f}%")
    print(f"  {'─'*50}")
    print(f"  kappa_P (center):        {kappa_center:.0f} m^-1")
    print(f"  Optical thickness tau:   {tau:.0f}")
    print(f"  {'='*50}\n")


