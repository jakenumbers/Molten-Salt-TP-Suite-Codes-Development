# 3. CASE STUDY 1 — Thermal Conductivity Decomposition
# =============================================================================

def case_1_conductivity_decomposition(
        T_array: np.ndarray,
        kappa_array: np.ndarray,
        n_array: np.ndarray,
        salt_name: str,
        salt_props: dict,
        L_lfa: Optional[float] = None,
        output_dir: Optional[str] = None):
    """
    Decompose the measured LFA thermal conductivity into phonon + radiative.

    What this shows:
        A standard LFA measures k_total = k_phonon + k_rad.
        If radiation is not accounted for, all of k_total is attributed
        to phonon conduction — overestimating k_phonon.

        This plot shows:
        - k_phonon(T) alone (what conduction really is)
        - k_rad(T) from the absorption model (what radiation adds)
        - k_total(T) = k_phonon + k_rad (what LFA reports)
        - Published LFA data for comparison

    The gap between k_phonon and k_total IS the radiative error.
    """
    if L_lfa is None:
        L_lfa = salt_props.get('L_lfa', 0.002)
    if output_dir is None:
        try:
            script_dir = os.path.dirname(os.path.abspath(__file__))
        except NameError:
            script_dir = os.getcwd()
        output_dir = os.path.join(script_dir, 'Figures')
    os.makedirs(output_dir, exist_ok=True)

    T_fus = salt_props['T_fus']
    k_ref = salt_props['k_phonon_ref']
    k_exp = salt_props['k_phonon_exp']

    # Only use temperatures above melting
    mask = T_array >= T_fus
    T = T_array[mask]
    kappa = kappa_array[mask]
    n_eff = n_array[mask]

    if len(T) < 2:
        print(f"  Warning: fewer than 2 temperatures above T_fus for {salt_name}")
        return None

    # Compute components
    k_ph = np.array([k_phonon(Ti, T_fus, k_ref, k_exp) for Ti in T])
    k_r = np.array([k_rad_deissler(Ti, ni, ki, L_lfa)
                     for Ti, ni, ki in zip(T, n_eff, kappa)])
    k_total = k_ph + k_r

    # Optical thickness at each temperature
    tau = kappa * L_lfa

    # Radiative fraction at highest temperature
    rad_frac_max = k_r[-1] / k_total[-1] * 100 if k_total[-1] > 0 else 0

    # ---- Plot ----
    fig, ax = plt.subplots(figsize=(7, 5), constrained_layout=True)

    # Phonon-only baseline
    ax.plot(T, k_ph, 'b--', linewidth=1.5,
            label='$k_{\\mathrm{phonon}}(T)$ (conduction only)')

    # Radiative component
    ax.plot(T, k_r, 'r:', linewidth=1.5,
            label=f'$k_{{\\mathrm{{rad}}}}(T)$ (Deissler, L={L_lfa*1e3:.0f} mm)')

    # Total = what LFA measures
    ax.plot(T, k_total, 'k-', linewidth=2.0,
            label='$k_{\\mathrm{total}} = k_{\\mathrm{phonon}} + k_{\\mathrm{rad}}$'
                  ' (LFA measures this)')

    # Shade the radiative contribution
    ax.fill_between(T, k_ph, k_total, alpha=0.15, color='red',
                    label='Radiative contribution')

    # Published LFA data
    published = salt_props.get('published_k', [])
    for pub in published:
        T_pub = np.array(pub['T_K'])
        k_pub = np.array(pub['k'])
        ax.scatter(T_pub, k_pub,
                   marker=pub.get('marker', 'o'),
                   color=pub.get('color', 'green'),
                   s=60, edgecolors='black', linewidth=0.5,
                   zorder=5, alpha=0.8,
                   label=pub.get('label', 'Published LFA'))

    # Annotate radiative fraction
    ax.annotate(
        f'Radiative fraction\nat {T[-1]:.0f} K: {rad_frac_max:.1f}%',
        xy=(T[-1], k_total[-1]),
        xytext=(-120, 30), textcoords='offset points',
        fontsize=9,
        arrowprops=dict(arrowstyle='->', color='red', lw=1.2),
        bbox=dict(boxstyle='round,pad=0.3', facecolor='lightyellow',
                  edgecolor='red', alpha=0.8))

    # Annotate optical thickness range
    ax.text(0.03, 0.03,
            f'$\\tau$ range: {tau[0]:.1f} – {tau[-1]:.1f}\n'
            f'L = {L_lfa*1e3:.0f} mm',
            transform=ax.transAxes, fontsize=8,
            verticalalignment='bottom',
            bbox=dict(boxstyle='round,pad=0.3', facecolor='wheat', alpha=0.6))

    ax.set_xlabel('Temperature (K)')
    ax.set_ylabel('Thermal Conductivity (W/m$\\cdot$K)')
    ax.set_title(f'{salt_name}: LFA Thermal Conductivity Decomposition',
                 fontweight='bold')
    ax.legend(loc='best', fontsize=8, framealpha=0.9)
    ax.set_xlim(T[0], T[-1])
    ax.set_ylim(bottom=0)

    filepath = os.path.join(output_dir, f'Case1_k_decomposition_{salt_name}.pdf')
    fig.savefig(filepath, bbox_inches='tight', dpi=300)
    print(f"  Saved: {filepath}")

    return fig


