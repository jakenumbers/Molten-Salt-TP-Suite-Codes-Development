# 4. CASE STUDY 2 — Sensitivity to LFA Sample Thickness
# =============================================================================

def case_2_thickness_sensitivity(
        T_array: np.ndarray,
        kappa_array: np.ndarray,
        n_array: np.ndarray,
        salt_name: str,
        salt_props: dict,
        L_values: Optional[List[float]] = None,
        output_dir: Optional[str] = None):
    """
    Sensitivity of measured k_total to LFA sample thickness.

    Thinner samples have smaller optical thickness tau = kappa * L,
    so the Deissler correction is relatively larger — more radiation
    crosses the sample, inflating the apparent thermal conductivity.

    This directly shows why LFA measurements at different sample
    thicknesses can give different results for semi-transparent salts.
    """
    if L_values is None:
        L_values = [0.001, 0.002, 0.003]   # 1, 2, 3 mm
    if output_dir is None:
        try:
            script_dir = os.path.dirname(os.path.abspath(__file__))
        except NameError:
            script_dir = os.getcwd()
        output_dir = os.path.join(script_dir, 'Figures')
    os.makedirs(output_dir, exist_ok=True)

    T_fus = salt_props['T_fus']
    k_ref = salt_props['k_phonon_ref']
    k_exp = salt_props['k_phonon_exp']

    mask = T_array >= T_fus
    T = T_array[mask]
    kappa = kappa_array[mask]
    n_eff = n_array[mask]

    if len(T) < 2:
        print(f"  Warning: fewer than 2 temperatures above T_fus for {salt_name}")
        return None

    # Phonon baseline
    k_ph = np.array([k_phonon(Ti, T_fus, k_ref, k_exp) for Ti in T])

    # Reference kappa for tau annotation
    idx_mid = len(T) // 2
    kappa_mid = kappa[idx_mid]

    # ---- Plot ----
    fig, ax = plt.subplots(figsize=(7, 5), constrained_layout=True)

    # Phonon-only baseline
    ax.plot(T, k_ph, 'k:', linewidth=2.0,
            label='$k_{\\mathrm{phonon}}$ (conduction only)')

    colors = ['#1f77b4', '#ff7f0e', '#2ca02c', '#d62728']
    styles = ['-', '--', '-.', ':']

    for j, L in enumerate(L_values):
        k_total = np.empty_like(T)
        for i, (Ti, ni, ki) in enumerate(zip(T, n_eff, kappa)):
            k_r = k_rad_deissler(Ti, ni, ki, L)
            k_total[i] = k_ph[i] + k_r

        tau_mid = kappa_mid * L
        label = (f'$k_{{\\mathrm{{total}}}}$, L = {L*1e3:.0f} mm '
                 f'($\\tau_{{\\mathrm{{mid}}}}$ = {tau_mid:.1f})')
        ax.plot(T, k_total,
                color=colors[j % len(colors)],
                linestyle=styles[j % len(styles)],
                linewidth=1.5, label=label)

    # Published data
    published = salt_props.get('published_k', [])
    for pub in published:
        T_pub = np.array(pub['T_K'])
        k_pub = np.array(pub['k'])
        ax.scatter(T_pub, k_pub,
                   marker=pub.get('marker', 'o'),
                   color=pub.get('color', 'green'),
                   s=60, edgecolors='black', linewidth=0.5,
                   zorder=5, alpha=0.8,
                   label=pub.get('label', 'Published LFA'))

    ax.set_xlabel('Temperature (K)')
    ax.set_ylabel('Thermal Conductivity (W/m$\\cdot$K)')
    ax.set_title(f'{salt_name}: Sensitivity to LFA Sample Thickness',
                 fontweight='bold')
    ax.legend(loc='best', fontsize=8, framealpha=0.9)
    ax.set_xlim(T[0], T[-1])
    ax.set_ylim(bottom=0)

    filepath = os.path.join(output_dir,
                            f'Case2_thickness_sensitivity_{salt_name}.pdf')
    fig.savefig(filepath, bbox_inches='tight', dpi=300)
    print(f"  Saved: {filepath}")

    return fig


