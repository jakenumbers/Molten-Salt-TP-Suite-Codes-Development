def load_salt_data(salt_name: str,
                   filename: str = 'Salt_RHT_Properties.csv'
                   ) -> Tuple[np.ndarray, np.ndarray, np.ndarray]:
    """
    Load temperature-dependent kappa_P and n_eff from the absorption model CSV.

    Returns
    -------
    T_array     : temperature array [K]
    kappa_array : Planck-mean absorption coefficient [m^-1]
    n_array     : Planck-mean refractive index
    """
    try:
        script_dir = os.path.dirname(os.path.abspath(__file__))
    except NameError:
        script_dir = os.getcwd()
    full_path = os.path.join(script_dir, filename)

    if not os.path.exists(full_path):
        raise FileNotFoundError(
            f"Database not found: {full_path}\n"
            "Run the absorption model first to generate Salt_RHT_Properties.csv.")

    df = pd.read_csv(full_path)
    row = df[df['Salt'] == salt_name]
    if row.empty:
        available = df['Salt'].unique().tolist()
        raise ValueError(
            f"Salt '{salt_name}' not in database. Available: {available}")

    row = row.iloc[0]
    T = np.array([float(x) for x in str(row['Temperature_List_K']).split(',')])
    kappa = np.array([float(x) for x in str(row['Absorption_List_m1']).split(',')])
    n = np.array([float(x) for x in str(row['Refractive_Index_List']).split(',')])

    return T, kappa, n


def density_at_T(rho_coeffs: Tuple[float, float], T: float) -> float:
    """Linear density: rho(T) = a + b*T [kg/m^3]."""
    return rho_coeffs[0] + rho_coeffs[1] * T


def cp_at_T(cp_input, T: float) -> float:
    """Specific heat: constant or linear cp(T) = a + b*T [J/kg/K]."""
    if isinstance(cp_input, (int, float)):
        return float(cp_input)
    return cp_input[0] + cp_input[1] * T


def k_phonon(T: float, T_fus: float, k_ref: float, exponent: float) -> float:
    """
    Phonon thermal conductivity (Gheribi et al., 2014):
        k_phonon(T) = k_ref * (T_fus / T)^exponent

    Gives decreasing k with temperature — expected for phonon conduction
    in liquids where increased thermal motion disrupts heat transfer.
    """
    if T <= 0:
        return k_ref
    return k_ref * (T_fus / T) ** exponent


def k_rad_deissler(T: float, n: float, kappa_P: float, L: float) -> float:
    """
    Deissler bridging radiative conductivity [W/m/K]:

        k_rad = 16 n^2 sigma T^3 / (3 kappa_P + 4/L)

    Smoothly interpolates between:
        - Optically thick (tau >> 1): reduces to Rosseland
        - Optically thin  (tau << 1): radiation across gap ~ L

    Parameters
    ----------
    T       : temperature [K]
    n       : refractive index
    kappa_P : Planck-mean absorption coefficient [m^-1]
    L       : characteristic length (sample thickness) [m]
    """
    if kappa_P <= 0 and L <= 0:
        return 0.0
    denom = 3.0 * kappa_P + (4.0 / L if L > 0 else 0.0)
    if denom <= 0:
        return 0.0
    return 16.0 * n**2 * sigma_SB * T**3 / denom


def k_rad_rosseland(T: float, n: float, kappa_P: float) -> float:
    """
    Rosseland (optically thick) radiative conductivity [W/m/K]:

        k_rad = 16 n^2 sigma T^3 / (3 kappa_P)

    Valid only when optical thickness tau = kappa_P * L >> 1.
    """
    if kappa_P <= 0:
        return 0.0
    return 16.0 * n**2 * sigma_SB * T**3 / (3.0 * kappa_P)


