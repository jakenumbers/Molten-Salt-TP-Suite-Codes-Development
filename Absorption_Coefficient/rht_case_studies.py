# 5. SUMMARY TABLE
# =============================================================================

def print_summary_table(T_array: np.ndarray,
                        kappa_array: np.ndarray,
                        n_array: np.ndarray,
                        salt_name: str,
                        salt_props: dict,
                        L_lfa: Optional[float] = None):
    """Print a formatted table of key radiative metrics at select temperatures."""
    if L_lfa is None:
        L_lfa = salt_props.get('L_lfa', 0.002)

    T_fus = salt_props['T_fus']
    k_ref = salt_props['k_phonon_ref']
    k_exp = salt_props['k_phonon_exp']

    mask = T_array >= T_fus
    T = T_array[mask]
    kappa = kappa_array[mask]
    n_eff = n_array[mask]

    # Pick ~8 evenly spaced temperatures
    n_pts = min(8, len(T))
    indices = np.linspace(0, len(T) - 1, n_pts, dtype=int)

    print(f"\n{'='*85}")
    print(f"  Radiative Thermal Conductivity Summary: {salt_name}")
    print(f"  LFA sample thickness = {L_lfa*1e3:.1f} mm")
    print(f"{'='*85}")
    print(f"  {'T [K]':>7}  {'kP [m-1]':>10}  {'n_eff':>7}  {'tau':>7}  "
          f"{'k_ph':>7}  {'k_rad':>7}  {'k_tot':>7}  {'rad%':>6}")
    print(f"  {'-'*7}  {'-'*10}  {'-'*7}  {'-'*7}  "
          f"{'-'*7}  {'-'*7}  {'-'*7}  {'-'*6}")

    for idx in indices:
        Ti = T[idx]
        ki = kappa[idx]
        ni = n_eff[idx]

        tau = ki * L_lfa
        k_ph = k_phonon(Ti, T_fus, k_ref, k_exp)
        k_r = k_rad_deissler(Ti, ni, ki, L_lfa)
        k_tot = k_ph + k_r
        rad_pct = k_r / k_tot * 100 if k_tot > 0 else 0

        print(f"  {Ti:>7.0f}  {ki:>10.1f}  {ni:>7.4f}  {tau:>7.2f}  "
              f"{k_ph:>7.3f}  {k_r:>7.4f}  {k_tot:>7.3f}  {rad_pct:>6.1f}")

    print(f"{'='*85}\n")


