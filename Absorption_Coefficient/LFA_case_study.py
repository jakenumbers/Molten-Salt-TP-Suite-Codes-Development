# 3. CASE STUDY 1: THERMAL CONDUCTIVITY DECOMPOSITION
# =============================================================================

def case_1_conductivity_decomposition(
        T_arr: np.ndarray,
        kappa_arr: np.ndarray,
        n_arr: np.ndarray,
        salt_name: str,
        salt_props: dict,
        output_dir: str) -> plt.Figure:
    """
    Decompose the LFA-measured thermal conductivity into conduction and
    radiative components.

    What this plot shows:
        - k_conduction(T): decreasing with T (Gheribi model)
        - k_rad(T): increasing with T (proportional to T^3 / kappa_P)
        - k_total(T) = k_conduction + k_rad: what LFA actually measures
        - Red shading: the radiative "error" that standard LFA doesn't separate
        - Published LFA data: for comparison with k_total

    Physics insight:
        The conduction part decreases while the radiative part increases with T.
        At some crossover temperature, radiation dominates.  Published LFA
        measurements that show a flat or increasing k(T) may be capturing
        this radiative contribution, not an increase in conduction conduction.
    """
    L = salt_props.get('L_lfa', 0.002)
    T_fus = salt_props['T_fus']

    # Only analyze above melting point
    mask = T_arr >= T_fus
    T = T_arr[mask]
    kappa = kappa_arr[mask]
    n_eff = n_arr[mask]

    if len(T) < 2:
        print(f"  Skipping {salt_name}: not enough data above T_fus={T_fus} K")
        return None

    # Compute components
    k_cond = np.array([k_conduction(Ti, T_fus, salt_props['k_conduction_ref'],
                               salt_props['k_conduction_slope']) for Ti in T])
    k_rd = np.array([k_rad_deissler(Ti, ni, ki, L)
                      for Ti, ni, ki in zip(T, n_eff, kappa)])
    k_total = k_cond + k_rd

    # Optical thickness at midpoint for annotation
    i_mid = len(T) // 2
    tau_mid = kappa[i_mid] * L
    tau_max = kappa[-1] * L

    # Radiative fraction at highest temperature
    rad_frac_max = k_rd[-1] / k_total[-1] * 100 if k_total[-1] > 0 else 0

    # ---- Plot ----
    fig, ax = plt.subplots(figsize=(6, 4.5), constrained_layout=True)

    # Conduction conduction baseline
    ax.plot(T, k_cond, 'b--', linewidth=1.5,
            label='$k_{\\mathrm{cond}}(T)$')

    # Radiative contribution
    ax.plot(T, k_rd, 'r:', linewidth=1.5,
            label='$k_{\\mathrm{rad}}(T)$ (Deissler, '
                  f'L = {L*1e3:.0f} mm)')

    # Total = what LFA measures
    ax.plot(T, k_total, 'k-', linewidth=2.0,
            label='$k_{\\mathrm{total}}$ = $k_{\\mathrm{cond}}$ + '
                  '$k_{\\mathrm{rad}}$')

    # Shading for radiative contribution
    ax.fill_between(T, k_cond, k_total, color='red', alpha=0.12,
                    label=f'Radiative contribution '
                          f'({rad_frac_max:.1f}% at {T[-1]:.0f} K)')

    # Published data overlay
    published = salt_props.get('published_k', [])
    default_markers = ['o', 's', '^', 'v', 'D', '*']
    default_colors = ['green', 'purple', 'orange', 'brown', 'teal', 'gray']
    for i, pub in enumerate(published):
        mk = pub.get('marker', default_markers[i % len(default_markers)])
        # Avoid using blue or red for published-data markers so they don't
        # get confused with the model spectrum lines. Use default palette
        # when authors specified 'blue' or 'red'. Make markers translucent.
        supplied_clr = pub.get('color', None)
        if supplied_clr is None:
            cl = default_colors[i % len(default_colors)]
        else:
            sc = str(supplied_clr).lower()
            if sc in ('blue', 'b', '#0000ff', 'red', 'r', '#ff0000'):
                cl = default_colors[i % len(default_colors)]
            else:
                cl = supplied_clr

        ax.scatter(pub['T_K'], pub['k'], marker=mk, color=cl,
                   s=56, edgecolors='black', linewidth=0.5,
                   zorder=5, alpha=0.65,
                   label=pub.get('label', f'Published {i+1}'))

    # Add summary info into legend instead of as an axes text box so it
    # remains inside the plot but does not overlap datapoints.
    info_label = (f"{salt_name} — L = {L*1e3:.0f} mm, "
                  f"$\\tau$ = {tau_mid:.1f}\u2013{tau_max:.1f}")
    # Create an invisible handle with the info label for the legend
    ax.plot([], [], ' ', label=info_label)

    # Attach the info label to the Figure so callers can access it later
    fig.info_label = info_label

    ax.set_xlabel('Temperature (K)')
    ax.set_ylabel('Thermal Conductivity (W m$^{-1}$ K$^{-1}$)')
     #ax.set_title('Case 1: LFA Thermal Conductivity Decomposition')
    place_legend_avoiding_points(ax, fontsize=9, first_label=info_label)

    pdf_path = os.path.join(output_dir,
                            f'Case1_k_decomposition_{salt_name}.pdf')
    png_path = os.path.join(output_dir,
                            f'Case1_k_decomposition_{salt_name}.png')
    fig.savefig(pdf_path, dpi=1200, bbox_inches='tight')
    fig.savefig(png_path, dpi=1200, bbox_inches='tight')
    print(f"  Saved: {pdf_path} and {png_path}")
    return fig


