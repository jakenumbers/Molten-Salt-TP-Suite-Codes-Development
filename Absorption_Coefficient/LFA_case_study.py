def case_2_thickness_sensitivity(
        T_arr: np.ndarray,
        kappa_arr: np.ndarray,
        n_arr: np.ndarray,
        salt_name: str,
        salt_props: dict,
        output_dir: str) -> plt.Figure:
    """
    Show how the LFA-measured thermal conductivity depends on sample
    thickness through the optical thickness tau = kappa_P * L.

    What this plot shows:
        - Thinner samples (smaller L) have smaller optical thickness
        - At low tau, more radiation crosses the sample, inflating k_measured
        - At high tau, the sample is effectively opaque and k_rad is small
        - The spread between curves quantifies the LFA thickness artifact

    Physics insight:
        If experimenters use different sample thicknesses and get different
        k values, it's a signature of semi-transparency.  The Deissler
        formula captures this thickness dependence correctly.
    """
    T_fus = salt_props['T_fus']

    mask = T_arr >= T_fus
    T = T_arr[mask]
    kappa = kappa_arr[mask]
    n_eff = n_arr[mask]

    if len(T) < 2:
        print(f"  Skipping {salt_name}: not enough data above T_fus")
        return None

    L_values = [0.001, 0.002, 0.003]  # 1, 2, 3 mm
    colors = ['#e74c3c', '#2ecc71', '#3498db']
    styles = ['-', '--', ':']

    # Conduction baseline (independent of L)
    k_cond = np.array([k_conduction(Ti, T_fus, salt_props['k_conduction_ref'],
                               salt_props['k_conduction_slope']) for Ti in T])

    fig, ax = plt.subplots(figsize=(6, 4.5), constrained_layout=True)

    # Conduction baseline
    ax.plot(T, k_cond, 'k:', linewidth=1.5, alpha=0.7,
            label='$k_{\\mathrm{cond}}$ only')

    # Reference kappa at midpoint for tau annotation
    i_mid = len(T) // 2

    for L, color, ls in zip(L_values, colors, styles):
        k_total = np.empty_like(T)
        for i, (Ti, ni, ki) in enumerate(zip(T, n_eff, kappa)):
            k_r = k_rad_deissler(Ti, ni, ki, L)
            k_total[i] = k_conduction(Ti, T_fus, salt_props['k_conduction_ref'],
                                   salt_props['k_conduction_slope']) + k_r

        tau_mid = kappa[i_mid] * L
        label = (f'L = {L*1e3:.0f} mm '
                 f'($\\tau_{{\\mathrm{{mid}}}}$ = {tau_mid:.1f})')
        ax.plot(T, k_total, color=color, linestyle=ls,
                linewidth=2.0, label=label)

    # Add summary info into the legend rather than an axes text box
    info_label = (f"{salt_name} — $\\kappa_P$ = {kappa[i_mid]:.0f} m$^{{-1}}$ "
                  f"at {T[i_mid]:.0f} K")
    ax.plot([], [], ' ', label=info_label)

    ax.set_xlabel('Temperature (K)')
    ax.set_ylabel('Apparent Thermal Conductivity (W m$^{-1}$ K$^{-1}$)')
     #ax.set_title('Case 2: LFA Sensitivity to Sample Thickness')
    place_legend_avoiding_points(ax, fontsize=9, first_label=info_label)

    pdf_path = os.path.join(output_dir,
                            f'Case2_thickness_{salt_name}.pdf')
    png_path = os.path.join(output_dir,
                            f'Case2_thickness_{salt_name}.png')
    fig.savefig(pdf_path, dpi=1200, bbox_inches='tight')
    fig.savefig(png_path, dpi=1200, bbox_inches='tight')
    print(f"  Saved: {pdf_path} and {png_path}")
    return fig


