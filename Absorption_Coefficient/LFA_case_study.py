def load_salt_data(salt_name: str,
                   filename: str = 'Salt_RHT_Properties.csv'
                   ) -> Tuple[np.ndarray, np.ndarray, np.ndarray]:
    """
    Load temperature-dependent kappa_P and n_eff from the absorption model CSV.

    Returns
    -------
    T_array     : temperature array [K]
    kappa_array : Planck-mean absorption coefficient [m^-1]
    n_array     : Planck-mean refractive index
    """
    try:
        script_dir = os.path.dirname(os.path.abspath(__file__))
    except NameError:
        script_dir = os.getcwd()
    full_path = os.path.join(script_dir, filename)

    if not os.path.exists(full_path):
        raise FileNotFoundError(
            f"Database not found: {full_path}\n"
            "Run the absorption model first to generate Salt_RHT_Properties.csv.")

    df = pd.read_csv(full_path)
    row = df[df['Salt'] == salt_name]
    if row.empty:
        available = df['Salt'].unique().tolist()
        raise ValueError(
            f"Salt '{salt_name}' not in database. Available: {available}")

    row = row.iloc[0]
    T = np.array([float(x) for x in str(row['Temperature_List_K']).split(',')])
    kappa = np.array([float(x) for x in str(row['Absorption_List_m1']).split(',')])
    n = np.array([float(x) for x in str(row['Refractive_Index_List']).split(',')])

    # Optional spectral arrays (wavelength [um], absorption spectrum, refractive index spectrum)
    wl_um = None
    alpha_spec = None
    n_spec = None
    if 'Wavelength_List_um' in row.index:
        try:
            wl_um = np.array([float(x) for x in str(row['Wavelength_List_um']).split(',')])
        except Exception:
            wl_um = None

    # Attempt to find a matching spectral column for a temperature near the melting point
    if wl_um is not None:
        # look for columns like 'Absorption_Spectrum_T1121K'
        spec_cols = [c for c in row.index if str(c).startswith('Absorption_Spectrum_T')]
        if spec_cols:
            # Find the spectrum column with temperature closest to T_fus (rounded)
            try:
                T_fus = float(row.get('T_fus_K', np.nan))
            except Exception:
                T_fus = np.nan

            best_col = None
            if not np.isnan(T_fus):
                best_diff = 1e9
                for c in spec_cols:
                    try:
                        tstr = c.replace('Absorption_Spectrum_T', '').replace('K', '')
                        tc = float(tstr)
                        diff = abs(tc - T_fus)
                        if diff < best_diff:
                            best_diff = diff
                            best_col = c
                    except Exception:
                        continue
            else:
                best_col = spec_cols[0]

            if best_col is None:
                best_col = spec_cols[0]

            try:
                alpha_spec = np.array([float(x) for x in str(row[best_col]).split(',')])
            except Exception:
                alpha_spec = None

            # corresponding refractive index column name
            n_col = best_col.replace('Absorption_Spectrum', 'Refractive_Index_Spectrum')
            if n_col in row.index:
                try:
                    n_spec = np.array([float(x) for x in str(row[n_col]).split(',')])
                except Exception:
                    n_spec = None

    return T, kappa, n, wl_um, alpha_spec, n_spec


def density_at_T(rho_coeffs: Tuple[float, float], T: float) -> float:
    """Linear density: rho(T) = a + b*T [kg/m^3]."""
    return rho_coeffs[0] + rho_coeffs[1] * T


def cp_at_T(cp_input, T: float) -> float:
    """Specific heat: constant or linear cp(T) = a + b*T [J/kg/K]."""
    if isinstance(cp_input, (int, float)):
        return float(cp_input)
    return cp_input[0] + cp_input[1] * T


def k_conduction(T: float, T_fus: float, k_ref: float, k_ref_B: float) -> float:
    """
    Conduction thermal conductivity (Gheribi et al., 2014):
        k_conduction(T) = k_ref * (T_fus / T)^exponent

    Gives decreasing k with temperature — expected for conduction conduction
    in liquids where increased thermal motion disrupts heat transfer.
    """
    if T <= 0:
        return k_ref
    return k_ref + k_ref_B*T


def k_rad_deissler(T: float, n: float, kappa_P: float, L: float) -> float:
    """
    Deissler bridging radiative conductivity [W/m/K]:

        k_rad = 16 n^2 sigma T^3 / (3 kappa_P + 4/L)

    Smoothly interpolates between:
        - Optically thick (tau >> 1): reduces to Rosseland
        - Optically thin  (tau << 1): radiation across gap ~ L

    Parameters
    ----------
    T       : temperature [K]
    n       : refractive index
    kappa_P : Planck-mean absorption coefficient [m^-1]
    L       : characteristic length (sample thickness) [m]
    """
    if kappa_P <= 0 and L <= 0:
        return 0.0
    denom = 3.0 * kappa_P + (4.0 / L if L > 0 else 0.0)
    if denom <= 0:
        return 0.0
    return 16.0 * n**2 * sigma_SB * T**3 / denom


def k_rad_spectral_deissler(wl_m: np.ndarray, T: float, L: float,
                            alpha_spec: Optional[np.ndarray] = None,
                            n_spec: Optional[np.ndarray] = None,
                            salt_obj=None) -> float:
    """
    Spectrally-resolved Deissler-like radiative conductivity.

    For each wavelength compute the Deissler denominator using the
    spectral absorption coefficient alpha(lambda) and refractive index n(lambda):

        k_rad(lambda) = 16 n(lambda)^2 sigma T^3 / (3 alpha(lambda) + 4/L)

    Then compute the Planck-weighted average:

        k_rad_spectral = integral( k_rad(lambda) * B_lambda(T) d_lambda )
                          / integral( B_lambda(T) d_lambda )

    This preserves spectral variation in alpha and n and is more accurate
    than using scalar Planck-mean kappa and n inside the nonlinear Deissler
    denominator.
    """
    # Compute spectral alpha and n from provided arrays or from the model
    if alpha_spec is None or n_spec is None:
        if salt_obj is None:
            raise ValueError('Either spectral arrays or a salt object must be provided')
        alpha_spec, n_spec = compute_alpha_total(wl_m, T, salt_obj)

    # Avoid zeros in alpha (treat as optically thin where appropriate)
    alpha_safe = np.maximum(alpha_spec, 1e-20)

    # Per-wavelength Deissler conductivity
    denom = 3.0 * alpha_safe + (4.0 / L if L > 0 else 0.0)
    k_rad_lambda = 16.0 * (n_spec ** 2) * sigma_SB * (T ** 3) / denom

    # Weight by Planck function
    B = planck_spectral_radiance(wl_m, T)
    num = np.trapezoid(k_rad_lambda * B, wl_m)
    den = np.trapezoid(B, wl_m)
    return float(num / den) if den > 0 else 0.0


def k_rad_rosseland(T: float, n: float, kappa_P: float) -> float:
    """
    Rosseland (optically thick) radiative conductivity [W/m/K]:

        k_rad = 16 n^2 sigma T^3 / (3 kappa_P)

    Valid only when optical thickness tau = kappa_P * L >> 1.
    """
    if kappa_P <= 0:
        return 0.0
    return 16.0 * n**2 * sigma_SB * T**3 / (3.0 * kappa_P)


