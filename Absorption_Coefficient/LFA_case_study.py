"""
LFA Radiative Thermal Conductivity Case Studies
================================================
Demonstrates how the predicted absorption coefficient and refractive index
affect the interpretation of LFA thermal conductivity measurements.

Physics:
    An LFA measurement yields an apparent thermal diffusivity alpha_app,
    which is converted to thermal conductivity via:

        k_measured = alpha_app * rho(T) * cp(T)

    If the salt is semi-transparent, the measured k includes BOTH conduction
    conduction and radiative transport:

        k_measured = k_conduction + k_rad

    where the Deissler bridging approximation gives:

        k_rad = 16 n^2 sigma T^3 / (3 kappa_P + 4/L)

    This reduces to the Rosseland diffusion limit when tau = kappa_P * L >> 1
    and to the optically-thin gap radiation when tau << 1.

    An LFA experiment that doesn't account for radiation attributes ALL
    of k_measured to conduction conduction, overestimating k_conduction.

Case Studies:
    Case 1 — Thermal conductivity decomposition: k_conduction vs k_rad vs k_total
             with published LFA data overlay for comparison.
    Case 2 — Sensitivity of k_total to LFA sample thickness (1, 2, 3 mm),
             showing how thinner samples see more radiative contribution.

References:
    Parker et al. (1961) J. Appl. Phys. 32(9), 1679-1684.
    Dong et al. (2024) Prog. Nat. Sci. Mater. Int. 34, 345-353.
    Robertson et al. (2022) Appl. Phys. Lett. 131(22), 225102.
    Gheribi et al. (2014) Sol. Energy Mater. Sol. Cells 126, 11-25.
    Deissler (1964) J. Heat Transfer 86(2), 240-246.

Dependencies:
    Requires 'Salt_RHT_Properties.csv' generated by the absorption model.
"""

