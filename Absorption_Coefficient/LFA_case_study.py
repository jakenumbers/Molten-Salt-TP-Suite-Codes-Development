def print_summary(T_arr, kappa_arr, n_arr, salt_name, salt_props):
    """Print key metrics at representative temperatures."""
    L = salt_props.get('L_lfa', 0.002)
    T_fus = salt_props['T_fus']
    mask = T_arr >= T_fus
    T, kappa, n_eff = T_arr[mask], kappa_arr[mask], n_arr[mask]

    if len(T) < 2:
        return

    n_pts = min(8, len(T))
    indices = np.linspace(0, len(T) - 1, n_pts, dtype=int)

    print(f"\n  {'T [K]':>7}  {'kP [m-1]':>10}  {'tau':>7}  "
          f"{'k_cond':>7}  {'k_rad':>7}  {'k_tot':>7}  {'rad%':>6}")
    print(f"  {'-'*7}  {'-'*10}  {'-'*7}  "
          f"{'-'*7}  {'-'*7}  {'-'*7}  {'-'*6}")

    for idx in indices:
        Ti, ki, ni = T[idx], kappa[idx], n_eff[idx]
        tau = ki * L
        k_cond = k_conduction(Ti, T_fus, salt_props['k_conduction_ref'],
                         salt_props['k_conduction_slope'])
        k_rd = k_rad_deissler(Ti, ni, ki, L)
        k_tot = k_cond + k_rd
        frac = k_rd / k_tot * 100 if k_tot > 0 else 0

        print(f"  {Ti:>7.0f}  {ki:>10.1f}  {tau:>7.2f}  "
              f"{k_cond:>7.3f}  {k_rd:>7.3f}  {k_tot:>7.3f}  {frac:>6.1f}")
    print()


